// Fonction utilitaire pour normaliser les chaînes (enlever accents, mettre en minuscule)
function normalizeString(str) {
  return str
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase();
}

// Fonction de recherche pour pays et régions
function searchRegions(searchTerm) {
  const normalizedSearch = normalizeString(searchTerm);
  const results = {
    countries: [],
    regions: [],
  };

  // Recherche dans les pays
  for (const countryKey in europeanRegions) {
    const countryName = formatCountryName(countryKey);
    const normalizedCountryName = normalizeString(countryName);

    if (normalizedCountryName.includes(normalizedSearch)) {
      results.countries.push({
        key: countryKey,
        name: countryName,
        regions: europeanRegions[countryKey],
      });
    }

    // Recherche dans les régions de ce pays
    for (const region of europeanRegions[countryKey]) {
      const normalizedRegionName = normalizeString(region.name);
      if (normalizedRegionName.includes(normalizedSearch)) {
        results.regions.push({
          countryKey: countryKey,
          countryName: countryName,
          region: region,
        });
      }
    }
  }

  return results;
}

// Formater le nom du pays à partir de la clé
function formatCountryName(countryKey) {
  return countryKey
    .replace(/-/g, ' ')
    .replace(/\b\w/g, l => l.toUpperCase())
    .replace('Royaume Uni', 'Royaume-Uni')
    .replace('Pays Bas', 'Pays-Bas')
    .replace('Republique Tcheque', 'République Tchèque')
    .replace('Bosnie Herzegovine', 'Bosnie-Herzégovine')
    .replace('Saint Marin', 'Saint-Marin');
}

